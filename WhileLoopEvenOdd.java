package JavaHomework3;

public class WhileLoopEvenOdd {
    public static void main(String[] args) {
        int o=1;
        int e=2;

        while (o<=20) {
            System.out.println("Odd Number:" + o);
            o+= 2;
        }
        while (e<=20) {
            System.out.println("Even Number:" + e);
            e+= 2;
        }
    }
}
