package JavaHomework3;

public class DoWhileLoopEvenOdd {
    public static void main(String[] args) {
        int o = 1;
        do {
            System.out.println("Odd Number:" + o);
            o += 2;
        } while (o <= 20) ;

        int e = 2;
        do {
            System.out.println("Even Number:" + e);
            e += 2;
        }while (e <= 20) ;
            }

}